package car;

public class CarDTO {
	private String car_company; // 제조회사
	private String car_name; // 자동차 이름
	private String car_color; // 자동차 색상
	private String car_cost; // 자동차 가격
	private String car_deliveryDate; // 자동차 출고날짜
	private String car_info; // 상세사양
	private String car_image; // 자동차이미지

	public String getCar_company() {
		return car_company;
	}

	public void setCar_company(String car_company) {
		this.car_company = car_company;
	}

	public String getCar_name() {
		return car_name;
	}

	public void setCar_name(String car_name) {
		this.car_name = car_name;
	}

	public String getCar_color() {
		return car_color;
	}

	public void setCar_color(String car_color) {
		this.car_color = car_color;
	}

	public String getCar_cost() {
		return car_cost;
	}

	public void setCar_cost(String car_cost) {
		this.car_cost = car_cost;
	}

	public String getCar_deliveryDate() {
		return car_deliveryDate;
	}

	public void setCar_deliveryDate(String car_deliveryDate) {
		this.car_deliveryDate = car_deliveryDate;
	}

	public String getCar_info() {
		return car_info;
	}

	public void setCar_info(String car_info) {
		this.car_info = car_info;
	}

	public String getCar_image() {
		return car_image;
	}

	public void setCar_image(String car_image) {
		this.car_image = car_image;
	}

}
